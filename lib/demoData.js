function act(id, name, sport, date, distKm, elevM, timeMin) {
  const d = new Date(date)
  return {
    id:                 `demo-${id}`,
    user_id:            'demo',
    strava_activity_id: String(id),
    name,
    sport_type:         sport,
    start_date:         d.toISOString(),
    distance_m:         distKm * 1000,
    elevation_gain_m:   elevM,
    moving_time_s:      timeMin * 60,
    year:               d.getFullYear(),
    month:              d.getMonth() + 1,
  }
}

